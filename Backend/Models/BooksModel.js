import mongoose from "mongoose";

const BookSchema = new mongoose.Schema(
  {
    BookName: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
    },
  },
  { timestamps: true }
);


export const Books = new mongoose.model("Books",BookSchema);